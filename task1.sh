#!/bin/bash

# 1.Список всех файлов с указанием типа
echo "--- Список файлов в текущей директории ---"
ls -F

# 2.Проверка наличия файла (имя файла передается первым аргументом)
echo -e "\n--- Проверка наличия файла $1 ---"
if [ -z "$1" ]; then
    echo "Ошибка: Вы не указали имя файла в качестве аргумента."
elif [ -e "$1" ]; then
    echo "Файл '$1' существует."
else
    echo "Файл '$1' не найден."
fi

# 3.Цикл for для вывода имени и прав доступа
echo -e "\n--- Права доступа файлов ---"
for file in *; do
    if [ -e "$file" ]; then
        echo "Файл: $file | Права: $(stat -c '%A' "$file" 2>/dev/null || ls -l "$file" | awk '{print $1}')"
    fi
done
