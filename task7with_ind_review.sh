#Автодополнение (Tab):
#Автодополнение позволяет не печатать длинные имена файлов.
#Пример: Если ввести cd De, а затем нажать клавишу Tab, терминал автоматически дополнит это до cd Desktop/ (если такая папка есть). Если вариантов несколько (например, Dev и Desktop), двойное нажатие Tab покажет список вариантов.

