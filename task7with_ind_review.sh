#!/bin/bash

# Создание alias
alias ll='ls -la'

echo "Alias 'll' создан. Пробуем выполнить:"
# Чтобы alias сработал внутри скрипта, нужно включить опцию expand_aliases,
# но обычно alias используются в интерактивном терминале.
shopt -s expand_aliases
ll


#Как сделать alias постоянным?
